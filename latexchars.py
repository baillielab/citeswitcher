def replace_accents(thistext, mode="biblatex"):
    if mode=="biblatex":
        switchlatex = {x[1]:x[0] for x in lc2}
    elif mode=="latex":
        switchlatex = {x[1]:x[0] for x in lc1}
    allchars = set(list(thistext))
    nonprintable = allchars - set(string.printable)
    for char in nonprintable:
        try:
            switchlatex[char]
        except:
            print ("unprintable character {} not found in latex character dict".format(char))
            continue
        thistext = thistext.replace(char, switchlatex[char])
    return thistext









